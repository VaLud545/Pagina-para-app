<p>Nosotros somos Dragon Work, un grupo de estudiantes de la carrera de Ingeniería en Sistemas, los cuales hemos
   desarrollado la aplicación Coin Manager debido a la falta de una herramienta específicamente diseñada para el
   publico en general, que les permita llevar un seguimiento preciso y conveniente de sus gastos, establecer
   presupuestos realistas, recibir alertas personalizadas y acceder a información relevante sobre sus hábitos de
   consumo.
   <br>
   <br>
   Creemos que todos nosotros debemos desarrollar habilidades financieras, por lo tanto, contribuiremos a mejorar la
   vida financiera de las personas y brindarles una herramienta práctica para gestionar sus finanzas.
   <br>
   <br>
   La aplicación se desarrolla utilizando tecnología moderna y un diseño intuitivo. Proporcionamos una interfaz fácil
   de usar que permite a las personas llevar un registro detallado de sus gastos, establecer presupuestos
   personalizados y recibir alertas personalizadas. Además, ofrecemos información y análisis sobre los hábitos de
   consumo de los usuarios para que puedan tomar decisiones más informadas sobre sus gastos.
</p>

