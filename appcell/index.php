<p>Ofrecemos una aplicación móvil para el público en general, la cual les permite llevar un control eficiente de sus
   gastos. La aplicación les permite registrar y categorizar sus gastos diarios, establecer presupuestos, recibir
   notificaciones de gastos excesivos y acceder a análisis y consejos financieros personalizados. También brindamos
   recursos educativos sobre gestión financiera y consejos para una vida financiera saludable.
   <br>
   <br>
   La idea surgió a partir de la identificación de una necesidad común entre los universitarios y personas en
   general: llevar un control eficiente de sus gastos. Muchas personas enfrentan desafíos económicos y buscan manejar
   sus finanzas de manera responsable, pero a menudo carecen de herramientas adecuadas para hacerlo.
   </p>
